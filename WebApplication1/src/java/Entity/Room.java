package Entity;

/**
 *
 * @author admin
 */
public class Room {
    private int rid;
    private String rname;

    public Room() {
    }

    public Room(int rid, String rname) {
        this.rid = rid;
        this.rname = rname;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public String getRname() {
        return rname;
    }

    public void setRname(String rname) {
        this.rname = rname;
    }

    @Override
    public String toString() {
        return "Room{" + "rid=" + rid + ", rname=" + rname + '}';
    }
    
    
    
}
